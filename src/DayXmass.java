public class DayXmass {


public static String getDay( int day){

    switch (day){

        case 1:
            return "First";
        case 2:
            return "Second";
        case 3:
            return "Third";
        case 4 :
            return "Fourth";
        case 5:
            return "Fifth";
        case 6:
            return "Sixth";
        case 7:
            return  "Seventh";
        case 8:
            return "Eight";
        case 9:
            return "Ninth";
        case 10:
            return "Tenth";
        case 11:
            return "Eleventh";
        case 12:
            return "Twelfth";

        default:
            return "";


    }

}


}
